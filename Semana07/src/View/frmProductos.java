    public void ListarCategoria() {
        CtrCategoria ctrCat = new CtrCategoria();
        List<Categoria> cat = ctrCat.listarCategoria();
        ArrayList<ComboBoxItem> items = new ArrayList<>();
        items.add(new ComboBoxItem("0", "Seleccionar"));
        for (Categoria categoria : cat) {
            int id = categoria.getId_categoria();
            String descripcion = categoria.getDescripcion();
            items.add(new ComboBoxItem(String.valueOf(id), descripcion));
        }
        DefaultComboBoxModel<ComboBoxItem> comboBoxModel = new DefaultComboBoxModel<>();
        for (ComboBoxItem item : items) {
            comboBoxModel.addElement(item);
        }
        txtCategoria.setModel(comboBoxModel);

    }

    public void listarProductos() {
        ctrProductos ctrProd = new ctrProductos();
        List<Productos> listProd = ctrProd.listarProductos();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Codigo");
        model.addColumn("Descripcion");
        model.addColumn("Cantidad");
        model.addColumn("Marca");
        model.addColumn("Modelo");
        model.addColumn("Precio");
        model.addColumn("Stock");
        model.addColumn("Estado");
        model.addColumn("Categoria");

        for (Productos productos : listProd) {
            String estado = "Inactivo";
            if (productos.getEstado() == 0) {
                estado = "Activo";
            }
            Object[] rowData = {
                productos.getId_producto(),
                productos.getCodigo(),
                productos.getDescripcion(),
                productos.getCantidad(),
                productos.getMarca(),
                productos.getModelo(),
                productos.getPrecio(),
                productos.getStock(),
                estado,
                productos.getId_categoria(),};
            model.addRow(rowData);
        }
        tabla_productos.setModel(model);

    }

    public void nuevo() {
        txtCodigo.setText("");
        txtDescripcion.setText("");
        txtCantidad.setText("");
        txtMarca.setText("");
        txtModelo.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        txtEstado.isSelected();
        this.ListarCategoria();
    }

