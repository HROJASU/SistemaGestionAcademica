    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        ctrProductos ctrProd = new ctrProductos();

        ComboBoxItem selectItem = (ComboBoxItem) txtCategoria.getSelectedItem();
        System.out.println("Valor seleccionado es: " + selectItem.getValue());
        Productos prod = new Productos();

        prod.setCodigo(txtCodigo.getText());
        prod.setDescripcion(txtDescripcion.getText());
        prod.setCantidad(Integer.parseInt(txtCantidad.getText()));
        prod.setMarca(txtMarca.getText());
        prod.setModelo(txtModelo.getText());
        prod.setPrecio(Double.parseDouble(txtPrecio.getText()));
        prod.setStock(Integer.parseInt(txtStock.getText()));
        prod.setId_categoria(Integer.parseInt(selectItem.getValue()));
        int estado = 0;
        if (txtEstado.isSelected()) {
            estado = 1;
        } else {
            estado = 0;
        }
        prod.setEstado(estado);

        if (idProducto == 0) {
            if (ctrProd.Guardar(prod)) {
                this.listarProductos();
                this.nuevo();
                JOptionPane.showMessageDialog(null, "Se Guardo Producto");
            } else {
                JOptionPane.showMessageDialog(null, "Error al Guardar Producto");
            }
        } else {
            prod.setId_producto(idProducto);
            if (ctrProd.Actualizar(prod)) {
                this.listarProductos();
                this.nuevo();
                idProducto = 0;
                JOptionPane.showMessageDialog(null, "Se Actualizo Producto");
            } else {
                JOptionPane.showMessageDialog(null, "Error al Actualizar Producto");
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

